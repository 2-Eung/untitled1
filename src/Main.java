public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        a += 5; // a = a + 5;
        b -= 1; // b = b - 1;
        System.out.println(a);
        System.out.println(b);

        b *= 4; // b = b * 2;
        System.out.println(b);

        b /= 3; // b = b / 2;
        System.out.println(b);

        b %= 1; // b = b % 1;
        System.out.println(b);
    }
}